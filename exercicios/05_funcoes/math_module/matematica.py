#Declarando função soma que recebe dois números como parâmetro
def soma(a, b):
    return a+b #retornando a soma dos dois números

#Declarando função subtrai que recebe dois números como parâmetro
def subtrai(a, b):
    return a-b #retornando a subtração dos dois números

#Declarando função multiplica que recebe dois números como parâmetro
def multiplica(a, b):
    return a*b #retornando a multiplicação dos dois números

#Declarando função divide que recebe dois números como parâmetro
def divide(a, b):
    return a/b #retornando a divisão dos dois números