#Declarando função soma_lista que recebe uma lista como parâmetro
def soma_lista(lista):
    return sum(lista) #Retornando a soma dos itens da lista

#Declarando função media_lista que recebe uma lista como parâmetro
def media_lista(lista):
    return sum(lista)/len(lista) #Retornando a média dos itens da lista, somando eles e dividindo pelo tamanho da lista

#Declarando função maior que recebe uma lista como parâmetro
def maior(lista):
    return max(lista) #Retornando o maior item da lista

