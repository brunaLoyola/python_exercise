#Faça um programa que leia um numero inteiro e mostre seu antecessor e seu sucessor

numero = int(input('Digite um número inteiro: '))
antecessor = numero - 1
sucessor  = numero + 1
print(f' O número digitado é {numero} \n Seu antecessor é {antecessor} \n Seu sucessor é {sucessor}')