#Receba dois números do usuário e calcule os resultados dos operadores aritméticos básicos.

numero01 = int(input('Digite o primeiro número: '))
numero02 = int(input('Digite o segundo número: '))

print(f'A soma é: { numero01 + numero02}')
print(f'A subtração é: { numero01 - numero02}')
print(f'A multiplicação é: { numero01 * numero02}')
print(f'A divisão comum é: { numero01 // numero02}')
print(f'A potência é: { numero01 ** numero02}')
print(f'O resto da divisão é: { numero01 % numero02}')